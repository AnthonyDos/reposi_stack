package com.pastrycertified.pastrycertified.api.src.utils;

/**
 * Enumération représentant les rôles des utilisateurs dans le système.
 * Les rôles possibles sont {@code USER} (utilisateur standard) et {@code ADMIN} (administrateur).
 */
public enum Role {
    /**
     * Rôle pour les utilisateurs standards.
     */
    USER,

    /**
     * Rôle pour les administrateurs.
     */
    ADMIN
}
